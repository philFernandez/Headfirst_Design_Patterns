public class TheaterLights {

    public void on() {
        System.out.println("Theater Lights are on");
    }

    public void off() {
        System.out.println("Theater Lights are off");
    }

    public void dim() {
        System.out.println("Theater Lights are dimming");
    }

    public String toString() {
        return "TheaterLights[ ]";
    }
}
